    private void jButtonEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnviarActionPerformed
        try {
            String mensagem = "\n" + jMensagemUsuario.getText();

            byte[] array;
           // System.out.println("TAM :" + mensagem.split(" ")[1].getBytes().length);
            int k = 3;
            if (!mensagem.contains("GETFILESLIST")) {
                array = new byte[mensagem.split(" ")[1].getBytes().length+3];
                byte[] nomeArq = mensagem.split(" ")[1].getBytes();
                array[2] = (byte) nomeArq.length;
                int tamanho = mensagem.split(" ")[1].getBytes().length;
                for (int j = 0; j < tamanho; j++) {
                    array[k++] = nomeArq[j];
                }
            }else{
                array = new byte[2];
            }
            
            array[0] = 1;
             
            if (mensagem.contains("DELETE")) {
                array[1] = 2;
            } else if (mensagem.contains("GETFILESLIST")) {
                array[1] = 3;
            } else if (mensagem.contains("GETFILE")) {
                array[1] = 4;
            }
            System.out.println("C - Comando: " + array[1]);
            System.out.println("C - Tamanho: " + array.length);

            out.write(array);
            jTodasMensagens.append(this.nome + " disse: " + jMensagemUsuario.getText() + "\n");
            jMensagemUsuario.setText("");
        } catch (IOException ex) {
            Logger.getLogger(JFrameConversa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonEnviarActionPerformed

