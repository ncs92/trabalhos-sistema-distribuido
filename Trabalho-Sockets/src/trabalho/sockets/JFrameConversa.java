    private void setMensagem(byte[] mensagem) {        
        System.out.println("RS - Comando: " + mensagem[0]);
        System.out.println("RS - Tamanho: " + mensagem.length);
        
        if(mensagem[1] == 2){
            if(mensagem[2] == 1){
                jTodasMensagens.append("\nArquivo excluído com sucesso!");
            }else{
                jTodasMensagens.append("\nArquivo não foi excluído!");
            }            
        }else if(mensagem[1] == 3){
            byte [] tamanho = new byte[2];
            tamanho[0] = mensagem[3];
            tamanho[1] = mensagem[4];
            System.out.println("AAA: " + tamanho[0] + tamanho[1]);
            int totalArquivos = new BigInteger(tamanho).intValue();;
            jTodasMensagens.append("\nTotal de arquivos: " + totalArquivos);
            jTodasMensagens.append("\nArquivos: ");
            int posAtual = 5;
            for(int i = 0; i < totalArquivos; i++){
                int tamanhoNome = (int) mensagem[posAtual];
                byte[] nome = new byte[tamanhoNome];
                posAtual++;
                for(int j = 0; j < tamanhoNome; j++){
                    nome[j] = mensagem[posAtual++];
                }
                String nomeArquivo = new String(nome);
                jTodasMensagens.append("\n" + nomeArquivo);
            }                           
        }else if(mensagem[1] == 4){
                      
        }
        
        // String[] partes = mensagem.split("-");
        //   jTodasMensagens.append(partes[0] + " disse: " + partes[1] + "\n");
    }

